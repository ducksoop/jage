#pragma once

#include <array>

namespace jage::input
{
	class Keyboard
	{
	public:
		/**
		 * Initializes the keyboard arrays to false, indicating no keys are pressed initially.
		 */
		static void Initialize();

		/**
		 * Updates the state of the keyboard keys by copying the current state to 'keys_last'
		 * and fetching the new current state from SDL.
		 */
		static void Update();

		/**
		 * Checks if a specific key is currently pressed.
		 * @param key The key index to check.
		 * @return True if the key is currently pressed, false otherwise.
		 */
		static bool Key(int key);

		/**
		 * Checks if a specific key was pressed this frame but not the last.
		 * @param key The key index to check.
		 * @return True if the key was pressed this frame, false otherwise.
		 */
		static bool KeyDown(int key);

		/**
		 * Checks if a specific key was released this frame.
		 * @param key The key index to check.
		 * @return True if the key was released this frame, false otherwise.
		 */
		static bool KeyUp(int key);

	private:
		constexpr static int key_count_ = 287;	// SDL supports up to index 286 - see SDL_NUM_SCANCODES

		// Static member definitions: storage for current and previous key states
		static std::array<bool, key_count_> keys_;
		static std::array<bool, key_count_> keys_last_;
	};
}

